    <h3>Apa itu Event Stack?</h3>
    <p>Event Stack adalah salah satu platform digital yang dapat kamu gunakan untuk mencari, membeli, mempublikasikan dan mengelola informasi peserta dan penyelenggaraan Event secara online.</p>

    <h3>Apakah ada biaya jika ingin menggunakan layanan yang disediakan Event Stack?</h3>
    <p>Event Stack tidak mewajibkan kamu membayar sejumlah uang ketika menggunakan sebagian besar layanan Event Stack. Event Stack gratis bagi siapapun. Namun, jika kamu menggunakan Event Stack untuk mempublikasikan Event berbayar, maka kami akan menetapkan biaya tertentu dari setiap tiket yang terjual melalui Event Stack.</p>

    <h3>Apakah saya harus mendaftar terlebih dahulu jika ingin mencari informasi mengenai suatu Event?</h3>
    <p>Tidak, kecuali kamu bermaksud untuk mengikuti Event tersebut.</p>

    <h3>Apa yang harus saya lakukan ketika saya ingin mengikuti suatu Event?</h3>
    <p>Kamu harus mendaftar terlebih dahulu untuk memiliki akun di Event Stack. Kemudian kamu bisa membeli tiket untuk menjadi peserta Event yang kamu pilih.</p>

    <h3>Bolehkah saya membeli tiket dengan jumlah lebih dari 1?</h3>
    <p>Bisa iya bisa tidak tergantung dari penyelenggara event (Stack Creator) mengatur model penjualan tiket</p>

