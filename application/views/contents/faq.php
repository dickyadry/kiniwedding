    <h3>Apakah saya bisa membatalkan pembelian tiket?</h3>
    <p>Tidak bisa, tiket yang sudah dipesan tidak dapat dibatalakan</p>

    <h3>Bagaimana jika saya mau mempublikasikan Event di Event Stack?</h3>
    <p>Kamu harus memiliki akun terlebih dahulu dengan cara mendaftar sebagai member di Event Stack. Setelah kamu membuat akun, kamu akan memiliki Dashboard atau halaman yang kami sediakan untuk mengelola semua aktifitas dan profilmu di Event Stack. Kemudian kamu dapat menggunakan menu “Buat Event” di Dashboard tersebut untuk membuat Event yang akan kamu publikasikan.</p>

    <h3>Jenis Event seperti apa yang dapat dikelola di Event Stack?</h3>
    <p>Event Stack adapat digunakan untuk mengelola pendaftaran dan pendaftaran hampir seluruh jenis event seperti seminar, training, penyuluhan, pengarahan, kajian, workshop, pertunjukan, tour/rekreasi, webinar, kompetisi, dan masih banyak lagi.</p>

    <h3>Berapa biaya yang dibebankan Event Stack untuk setiap 1 tiket Event berbayar?</h3>
    <p>Untuk saat ini, Event Stack belum memfasilitasi Event Berbayar. Skema harga akan dipublikasikan saat fitur untuk Event Berbayar dibuka.</p>

    <h3>Bagaimana jika saya ingin menyebarkan Event saya?</h3>
    <p>Kamu bisa sebarkan event kamu menggunakan fitur Share menggunakan akun sosmed kamu.</p>

    <h3>Apakah Event Stack bisa digunakan untuk menyelenggarakan Event dengan harga tiket yang berbeda-beda?</h3>
    <p>Ya, kamu dapat membuat skema harga untuk tiket kamu.</p>

